package com.example.visitnepal2020.Retrofit;

public class Bored {
    private int number;
    private String activity;

    public Bored(int number, String activity) {
        this.number = number;
        this.activity = activity;
    }

    public int getNumber() {
        return number;
    }

    public String getActivity() {
        return activity;
    }
}
